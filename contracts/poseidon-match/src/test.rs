#![cfg(test)]
//! Golden vectors from scripts/poseidon-golden/ (canonical iden3 circomlibjs).
//! If any assert fails, the on-chain Poseidon does NOT match circomlib and the
//! whole shielded-pool design is invalid — this is the Phase 0 gate (b) tripwire.

use super::*;
use soroban_sdk::{Bytes, Env, U256};

// circomlib Poseidon outputs (BN254 Fr, 32-byte big-endian).
const P1_OUT: [u8; 32] = [
    0x29, 0x17, 0x61, 0x00, 0xea, 0xa9, 0x62, 0xbd, 0xc1, 0xfe, 0x6c, 0x65, 0x4d, 0x6a, 0x3c, 0x13,
    0x0e, 0x96, 0xa4, 0xd1, 0x16, 0x8b, 0x33, 0x84, 0x8b, 0x89, 0x7d, 0xc5, 0x02, 0x82, 0x01, 0x33,
];
const P2_OUT: [u8; 32] = [
    0x11, 0x5c, 0xc0, 0xf5, 0xe7, 0xd6, 0x90, 0x41, 0x3d, 0xf6, 0x4c, 0x6b, 0x96, 0x62, 0xe9, 0xcf,
    0x2a, 0x36, 0x17, 0xf2, 0x74, 0x32, 0x45, 0x51, 0x9e, 0x19, 0x60, 0x7a, 0x44, 0x17, 0x18, 0x9a,
];
// Poseidon(2) with a near-modulus input: in0=7, in1=p-1.
const P2_BIG_IN1: [u8; 32] = [
    0x30, 0x64, 0x4e, 0x72, 0xe1, 0x31, 0xa0, 0x29, 0xb8, 0x50, 0x45, 0xb6, 0x81, 0x81, 0x58, 0x5d,
    0x28, 0x33, 0xe8, 0x48, 0x79, 0xb9, 0x70, 0x91, 0x43, 0xe1, 0xf5, 0x93, 0xf0, 0x00, 0x00, 0x00,
];
const P2_BIG_OUT: [u8; 32] = [
    0x01, 0x36, 0x7c, 0x36, 0xa4, 0x6d, 0x71, 0xa8, 0x0e, 0x0a, 0x03, 0x4b, 0x66, 0xf7, 0x73, 0xc5,
    0x93, 0x35, 0x5c, 0x86, 0x6d, 0x49, 0x19, 0xf2, 0x61, 0xd2, 0x54, 0x91, 0x9c, 0x13, 0xcf, 0x4c,
];
const P3_OUT: [u8; 32] = [
    0x0e, 0x77, 0x32, 0xd8, 0x9e, 0x69, 0x39, 0xc0, 0xff, 0x03, 0xd5, 0xe5, 0x8d, 0xab, 0x63, 0x02,
    0xf3, 0x23, 0x0e, 0x26, 0x9d, 0xc5, 0xb9, 0x68, 0xf7, 0x25, 0xdf, 0x34, 0xab, 0x36, 0xd7, 0x32,
];

fn u(env: &Env, n: u32) -> U256 {
    U256::from_u32(env, n)
}
fn u_be(env: &Env, bytes: &[u8; 32]) -> U256 {
    U256::from_be_bytes(env, &Bytes::from_array(env, bytes))
}

#[test]
fn poseidon_matches_circomlib() {
    let env = Env::default();
    let id = env.register(PoseidonMatch, ());
    let client = PoseidonMatchClient::new(&env, &id);

    // Poseidon(1)([1])
    assert_eq!(client.hash1(&u(&env, 1)), u_be(&env, &P1_OUT), "Poseidon(1) mismatch");

    // Poseidon(2)([1,2])
    assert_eq!(
        client.hash2(&u(&env, 1), &u(&env, 2)),
        u_be(&env, &P2_OUT),
        "Poseidon(2) mismatch"
    );

    // Poseidon(2)([7, p-1]) — near-modulus edge case
    assert_eq!(
        client.hash2(&u(&env, 7), &u_be(&env, &P2_BIG_IN1)),
        u_be(&env, &P2_BIG_OUT),
        "Poseidon(2) big-input mismatch"
    );

    // Poseidon(3)([1,2,3])
    assert_eq!(
        client.hash3(&u(&env, 1), &u(&env, 2), &u(&env, 3)),
        u_be(&env, &P3_OUT),
        "Poseidon(3) mismatch"
    );
}
